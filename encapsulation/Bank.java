public class Bank {
	private String holder_name;
	private int acc_no;

public void setname(String name) {
		holder_name=name;
	}
	public void setaccno(int accno) {
		acc_no=accno;
	}

	public String getname() {
		return holder_name;
	}
	public int getaccno() {
		return acc_no;
	}
	}
	
   
		
	

	     
	


